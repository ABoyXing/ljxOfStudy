import numpy as np
n1=np.array([1,2])
n2=np.array([3,4])
print(n1)
print(n2)
# 数组运算
print('加法运算',n1+n2)
print('减法运算',n1-n2)
print('乘法运算',n1*n2)
print('除法运算',n1/n2)
print('幂运算',n1**n2)
print('比较运算',n1>n2)
print('比较运算',n1<n2)
print('比较运算',n1!=n2)
#%%
